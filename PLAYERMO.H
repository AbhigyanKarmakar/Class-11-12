void input(int a[3][3],int x=0,int player=1)
{
  switch(x)
 {
   case 1 : a[0][0]=player;
	    break;

   case 2 : a[0][1]=player;
	    break;

   case 3 : a[0][2]=player;
	    break;

   case 4 : a[1][0]=player;
	    break;

   case 5 : a[1][1]=player;
	    break;

   case 6 : a[1][2]=player;
	    break;

   case 7 : a[2][0]=player;
	    break;

   case 8 : a[2][1]=player;
	    break;

   case 9 : a[2][2]=player;
	    break;

   default: break;
 }
 if (player==1)
 &player++;
 else
 &player--;
}